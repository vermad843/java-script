/*
push : adding at the end, 
unshift : adding at the front, 
pop : removing from the end,
shift :  removing from front
*/

//push 
let myArr = [1,2,3];
myArr.push(4);
console.log(myArr);


//unshift
myArr.unshift(4);
console.log(myArr);


//pop
myArr.pop();
console.log(myArr);


//shift
myArr.shift();
console.log(myArr);

